"""
gradient_inversion.py — Geiping et al. gradient inversion attack (RQ6).

Replicates the optimization-based gradient inversion attack from:
  Geiping et al., "Inverting Gradients — how easy is it to break
  privacy in federated learning?", NeurIPS 2020.

Evaluates image reconstruction fidelity under three conditions
(Section 7.6 of the CHRONOS paper):

  (a) Attacking the plaintext FedAvg gradient g_i (B1 baseline).
  (b) Attacking the CHRONOS-masked gradient g_tilde_i.
  (c) Attacking the recovered global aggregate G over N=20 clients.

Metrics:
  - PSNR (Peak Signal-to-Noise Ratio): lower = better privacy.
  - SSIM (Structural Similarity Index): lower = better privacy.

Usage:
    python gradient_inversion.py --condition plaintext --batch-size 1 \
           --dataset cifar10 --model small_cnn --iterations 5000 --seed 42

    python gradient_inversion.py --condition masked --batch-size 1 \
           --dataset cifar10 --model small_cnn --iterations 5000 --seed 42

    python gradient_inversion.py --condition aggregate --batch-size 1 \
           --num-clients 20 --dataset cifar10 --model small_cnn \
           --iterations 5000 --seed 42
"""

