logger = logging.getLogger("chronos.attack")

FIELD_PRIME    = (1 << 31) - 1
SCALING_FACTOR = 1 << 16


# ---------------------------------------------------------------------------
#  Total Variation regularizer
# ---------------------------------------------------------------------------

def total_variation(x: torch.Tensor) -> torch.Tensor:
    """
    TV regularization to enforce spatial smoothness.
    Applied to the dummy image during optimization.
    """
    dx = torch.mean(torch.abs(x[:, :, :, :-1] - x[:, :, :, 1:]))
    dy = torch.mean(torch.abs(x[:, :, :-1, :] - x[:, :, 1:, :]))
    return dx + dy


# ---------------------------------------------------------------------------
#  CHRONOS masking simulation
# ---------------------------------------------------------------------------

def simulate_chronos_mask(dimension: int, seed: int = 0) -> np.ndarray:
    """
    Simulate the AES-128-CTR pseudorandom mask from the TEE.
    Produces D uniformly random values in [0, p-1].

    In the real system this is generated by the TA using hardware-
    accelerated AES-128-CTR with rejection sampling.  For attack
    evaluation, a Python PRNG with the same statistical properties
    suffices — the attack's failure depends only on the mask being
    uniformly random, not on the specific generator.
    """
    rng = np.random.default_rng(seed)
    return rng.integers(0, FIELD_PRIME, size=dimension, dtype=np.int64)


def quantize_gradient(grad_fp32: np.ndarray) -> np.ndarray:
    """Quantize FP32 gradient to F_p."""
    scaled = np.round(grad_fp32 * SCALING_FACTOR).astype(np.int64)
    return (scaled + (FIELD_PRIME // 2)) % FIELD_PRIME


def dequantize_gradient(field_vec: np.ndarray) -> np.ndarray:
    """Convert F_p gradient back to FP32 (what the server observes)."""
    signed = field_vec.astype(np.int64) - (FIELD_PRIME // 2)
    return signed.astype(np.float64) / SCALING_FACTOR


# ---------------------------------------------------------------------------
#  Geiping attack
# ---------------------------------------------------------------------------

def geiping_attack(model: nn.Module, target_gradient: torch.Tensor,
                   original_image_shape: Tuple[int, ...],
                   known_label: int,
                   iterations: int = 5000,
                   lr: float = 0.1,
                   tv_weight: float = 0.1,
                   device: str = "cuda") -> torch.Tensor:
    """
    Optimization-based gradient inversion attack (Geiping et al., 2020).

    Minimizes: L = (1 - cos_sim(g_dummy, g_target)) + λ_TV · TV(x_dummy)

    When g_target is the CHRONOS-masked gradient, the AES-CTR uniform
    mask perfectly decorrelates the cosine similarity objective, forcing
    the optimizer into a flat loss landscape (Section 7.6).

    Args:
        model:               The shared model (in eval mode).
        target_gradient:     The gradient vector to invert (flat tensor).
        original_image_shape: Shape of the target image (C, H, W).
        known_label:         Ground-truth label (assumed known per Geiping).
        iterations:          Number of Adam optimizer steps.
        lr:                  Learning rate for Adam.
        tv_weight:           Weight for total variation regularization.
        device:              Compute device.

    Returns:
        Reconstructed image tensor.
    """
    model = model.to(device).eval()
    target_gradient = target_gradient.to(device)

    # Initialize dummy image with random noise
    dummy_image = torch.randn(
        (1,) + original_image_shape,
        requires_grad=True, device=device,
    )
    dummy_label = torch.tensor([known_label], device=device)

    optimizer = torch.optim.Adam([dummy_image], lr=lr)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=iterations)

    best_loss = float("inf")
    best_image = dummy_image.clone().detach()

    for i in range(iterations):
        optimizer.zero_grad()

        # Forward pass on dummy image
        dummy_output = model(dummy_image)
        dummy_loss = F.cross_entropy(dummy_output, dummy_label)

        # Compute dummy gradient
        dummy_grads = torch.autograd.grad(
            dummy_loss, model.parameters(), create_graph=True)
        flat_dummy = torch.cat([g.flatten() for g in dummy_grads])

        # Cosine similarity loss: 1 - cos(θ)
        cos_sim = F.cosine_similarity(
            flat_dummy.unsqueeze(0),
            target_gradient.unsqueeze(0),
        )
        grad_loss = 1.0 - cos_sim.mean()

        # Total loss = gradient matching + TV regularization
        loss = grad_loss + tv_weight * total_variation(dummy_image)

        loss.backward()
        optimizer.step()
        scheduler.step()

        if loss.item() < best_loss:
            best_loss = loss.item()
            best_image = dummy_image.clone().detach()

        if i % 500 == 0:
            logger.info("Iteration %d/%d | Loss: %.6f | Cos-sim: %.6f",
                         i, iterations, loss.item(), cos_sim.item())

    return best_image


# ---------------------------------------------------------------------------
#  Experiment runner
# ---------------------------------------------------------------------------

def run_attack_experiment(args) -> Dict:
    """
    Run the gradient inversion attack under the specified condition.

    Returns a dict with PSNR and SSIM metrics.
    """
    device = args.device
    torch.manual_seed(args.seed)
    np.random.seed(args.seed)

    # Load a single CIFAR-10 batch
    transform = transforms.Compose([
        transforms.ToTensor(),
    ])
    dataset = torchvision.datasets.CIFAR10(
        root=os.environ.get("CHRONOS_DATA_ROOT", "./data"),
        train=True, download=True, transform=transform)

    # Select target images
    loader = DataLoader(dataset, batch_size=args.batch_size, shuffle=False)
    target_images, target_labels = next(iter(loader))
    target_images = target_images.to(device)
    target_labels = target_labels.to(device)

    # Build model
    if args.model == "small_cnn":
        model = SmallCNN(num_classes=10).to(device)
    else:
        model = MediumCNN(num_classes=10).to(device)

    model.eval()

    # Compute true gradient
    output = model(target_images)
    loss = F.cross_entropy(output, target_labels)
    true_grads = torch.autograd.grad(loss, model.parameters())
    flat_true = torch.cat([g.flatten() for g in true_grads]).detach()

    D = flat_true.numel()
    logger.info("Model dimension D = %d", D)

    # Prepare the gradient the attacker observes
    if args.condition == "plaintext":
        # Condition (a): attacker sees raw gradient g_i
        attacker_gradient = flat_true.clone()

    elif args.condition == "masked":
        # Condition (b): attacker sees masked gradient g_tilde_i
        grad_np = flat_true.cpu().numpy()
        quantized = quantize_gradient(grad_np)

        # Apply CHRONOS mask
        mask = simulate_chronos_mask(D, seed=args.seed + 1000)
        masked = (quantized + mask) % FIELD_PRIME

        # Dequantize (what the server/attacker actually sees)
        observed_fp = dequantize_gradient(masked)
        attacker_gradient = torch.tensor(
            observed_fp, dtype=torch.float32, device=device)

    elif args.condition == "aggregate":
        # Condition (c): attacker sees the global aggregate G
        # Simulate N clients with independent gradients
        all_grads = [flat_true.clone()]

        for c in range(1, args.num_clients):
            torch.manual_seed(args.seed + c)
            # Random images for other clients
            fake_imgs = torch.randn_like(target_images)
            fake_labels = torch.randint(0, 10, (args.batch_size,), device=device)
            out_c = model(fake_imgs)
            loss_c = F.cross_entropy(out_c, fake_labels)
            grads_c = torch.autograd.grad(loss_c, model.parameters())
            flat_c = torch.cat([g.flatten() for g in grads_c]).detach()
            all_grads.append(flat_c)

        # FedAvg aggregate (unweighted for simplicity)
        aggregate = torch.stack(all_grads).mean(dim=0)
        attacker_gradient = aggregate

    else:
        raise ValueError(f"Unknown condition: {args.condition}")

    # Run the Geiping attack
    logger.info("Running Geiping attack (condition=%s, iters=%d)...",
                 args.condition, args.iterations)

    reconstructed = geiping_attack(
        model=model,
        target_gradient=attacker_gradient,
        original_image_shape=target_images.shape[1:],
        known_label=target_labels[0].item(),
        iterations=args.iterations,
        lr=args.lr,
        tv_weight=args.tv_weight,
        device=device,
    )

    # Compute metrics
    # Clamp to [0,1] for fair comparison
    recon_clamped = reconstructed.clamp(0, 1)
    original = target_images[:1]

    psnr_val = calculate_psnr(original, recon_clamped)
    ssim_val = calculate_ssim(original, recon_clamped)

    results = {
        "condition": args.condition,
        "batch_size": args.batch_size,
        "model": args.model,
        "D": D,
        "iterations": args.iterations,
        "seed": args.seed,
        "psnr_dB": round(psnr_val, 2),
        "ssim": round(ssim_val, 4),
    }

    logger.info("Results: PSNR = %.2f dB, SSIM = %.4f",
                 psnr_val, ssim_val)

    return results


# ---------------------------------------------------------------------------
#  Batch runner for reproducing Table/Figure 4
# ---------------------------------------------------------------------------

def run_full_evaluation(args):
    """
    Run the attack across all conditions and batch sizes,
    repeating with 5 seeds as in the paper.
    """
    all_results = []
    seeds = list(range(args.seed, args.seed + 5))

    for condition in ["plaintext", "masked", "aggregate"]:
        for bs in [1, 8]:
            for seed in seeds:
                args.condition = condition
                args.batch_size = bs
                args.seed = seed

                result = run_attack_experiment(args)
                all_results.append(result)

    # Summary statistics
    print("\n" + "=" * 70)
    print("  Gradient Inversion Attack Results (RQ6)")
    print("=" * 70)
    print(f"{'Condition':<12} {'BS':>3} | {'PSNR (dB)':>12} {'SSIM':>10}")
    print("-" * 42)

    for condition in ["plaintext", "masked", "aggregate"]:
        for bs in [1, 8]:
            subset = [r for r in all_results
                      if r["condition"] == condition and r["batch_size"] == bs]
            psnrs = [r["psnr_dB"] for r in subset]
            ssims = [r["ssim"] for r in subset]

            psnr_mean = np.mean(psnrs)
            psnr_ci = 1.96 * np.std(psnrs) / np.sqrt(len(psnrs))
            ssim_mean = np.mean(ssims)
            ssim_ci = 1.96 * np.std(ssims) / np.sqrt(len(ssims))

            print(f"{condition:<12} {bs:>3} | "
                  f"{psnr_mean:>5.1f} ± {psnr_ci:>4.1f}  "
                  f"{ssim_mean:>5.3f} ± {ssim_ci:>.3f}")

    # Save raw results
    out_path = Path(args.output_dir) / "gradient_inversion_results.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        json.dump(all_results, f, indent=2)
    print(f"\nRaw results saved to {out_path}")

    return all_results


