def main():
    parser = argparse.ArgumentParser(
        description="CHRONOS Gradient Inversion Attack (RQ6)")
    parser.add_argument("--condition", type=str, default="plaintext",
                        choices=["plaintext", "masked", "aggregate", "all"],
                        help="Attack condition")
    parser.add_argument("--batch-size", type=int, default=1,
                        help="Training batch size (BS in Figure 4)")
    parser.add_argument("--model", type=str, default="small_cnn",
                        choices=["small_cnn", "medium_cnn"])
    parser.add_argument("--dataset", type=str, default="cifar10")
    parser.add_argument("--num-clients", type=int, default=20,
                        help="N for aggregate condition")
    parser.add_argument("--iterations", type=int, default=5000,
                        help="Attack optimizer iterations")
    parser.add_argument("--lr", type=float, default=0.1,
                        help="Adam learning rate for attack")
    parser.add_argument("--tv-weight", type=float, default=0.1,
                        help="Total variation regularization weight")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--device", type=str, default="cuda"
                        if torch.cuda.is_available() else "cpu")
    parser.add_argument("--output-dir", type=str, default="./results")
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(name)s] %(levelname)s: %(message)s",
    )

    if args.condition == "all":
        run_full_evaluation(args)
    else:
        result = run_attack_experiment(args)
        print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()
